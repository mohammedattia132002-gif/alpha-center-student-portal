export interface GreetingResult {
  greetingTitle: string
  message: string
  category: keyof typeof CATEGORY_MESSAGES
  categoryLabel: string
  icon: string
}

const CATEGORY_META: Record<string, { label: string; icon: string }> = {
  motivational: { label: 'رسائل تحفيزية', icon: '📚' },
  studyTips: { label: 'نصائح دراسية', icon: '💡' },
  quotes: { label: 'حكم ملهمة', icon: '🌟' },
}

let lastCategory: string | null = null
let lastMessage: string | null = null

function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

function getRandomMessage(): { category: keyof typeof CATEGORY_MESSAGES; message: string } {
  const categories = Object.keys(CATEGORY_MESSAGES) as (keyof typeof CATEGORY_MESSAGES)[]

  let availableCategories = categories
  if (categories.length > 1 && lastCategory) {
    availableCategories = categories.filter(c => c !== lastCategory)
  }

  const category = pickRandom(availableCategories)
  const messages = CATEGORY_MESSAGES[category]

  let message: string
  if (messages.length > 1 && lastMessage) {
    const filtered = messages.filter(m => m !== lastMessage)
    message = pickRandom(filtered)
  } else {
    message = pickRandom(messages)
  }

  lastCategory = category
  lastMessage = message

  return { category, message }
}

export function getGreeting(): GreetingResult {
  const hour = new Date().getHours()

  let period: string
  let greetingTitle: string

  if (hour >= 5 && hour < 12) {
    period = 'morning'
    greetingTitle = '🌅 صباح الخير'
  } else if (hour >= 12 && hour < 17) {
    period = 'afternoon'
    greetingTitle = '☀️ نهارك سعيد'
  } else if (hour >= 17 && hour < 21) {
    period = 'evening'
    greetingTitle = '🌇 مساء الخير'
  } else {
    period = 'night'
    greetingTitle = '🌙 مساء هادئ'
  }

  const { category, message } = getRandomMessage()
  const meta = CATEGORY_META[category]

  return {
    greetingTitle,
    message,
    category,
    categoryLabel: meta.label,
    icon: meta.icon,
  }
}
